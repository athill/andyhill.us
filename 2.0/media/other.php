<a name="books"><b>Books</b></a>
  <p> I've read quite a bit in my time. These days I usually read books on how 
    to program computers or I read 
    <a href="http://www.it.usyd.edu.au/~matty/Shakespeare/">
    Shakespeare</a>'s plays. Shakespeare just rocks.
    Some of my favorite books are by 
    <a href="http://www.geocities.com/Athens/Forum/8506/">Bret Easton
    Ellis</a>, including <u>Less Than Zero</u>, <u>The Rules of Attraction</u>,
    <u>American Psycho</u> and <u>Glamorama</u>. His writing is pretty depraved, 
    and I'm not sure what draws me to it, but I've read LTZ, like ten times.
    I also like the writing of Nick Hornsby. He's written <u>High Fidelity</u> 
    and <u>About a Boy</u> among other things. He's very funny and has a 
    great command of British slang. I'd also highly recommend Jonathan Letham.
    He tends to put twists on classic genres and is very readable. Also fond 
    Jay McKinerney similar to B.E.E., but a little less depraved.  Some of his 
    better books include <u>Bright Lights, Big City</u>  and <u>Story of My Life</u>.
  </p>
  <p>I gotta give it up for Miss Manners, Judith Martin. While etiquette might not save the 
	world, it can make it more pleasant while we're here.  I also consider some of her answers to
	be quite clever and witty. Check her out at 
	<a href="http://www.womencentral.msn.com/firstperson/articles/missmanners.asp">
	Microsoft Women Central</a> or 
	<a href="http://www.washingtonpost.com/wp-dyn/style/columns/missmanners/"> 
	washintonpost.com</a>.<br />
  </p>

<a name="movies"><b>Movies</b></a>
<p>
Probably my two favorite movies
are <a href="http://www.fortunecity.com/meltingpot/regent/827/tbc.html"><u>The Breakfast 
Club</u></a> and <a href="http://www.cyhaus.com/pbride/pbride.htm"><u>The Princess Bride</u></a> Other movies I like include the films of Quentin Tarrintino, 
such as <u>Reservoir Dogs</u> and <u>Pulp Fiction</u>, the films of the 
<a href="http://www.d.umn.edu/~bjohns33/">Coen Brothers</a>, such as 
<u>Raising Arizona</u> and <u>The Man Who Wasn't There</u>, and the films of Tim Burton, such as 
<u>Batman</u> and <u>Sleepy Hollow</u>.  I also like the rest of John Hughes's ealy '80s movies as
well as the morbid classic, <u>Heather's</u>. Lately, I've become a fan of Carey Grant,
 especially, "Philidelphia Story." Basically, I mostly like romance, comedy, and noir. 
</p>
