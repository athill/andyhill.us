<a name="galleries" id="galleries"><h2>Galleries</h2></a>
Places to find original art in Bloomington include
The Belleview Gallery at 312 S. Washington, The By-Hand Gallery in Fountain Square Mall,
and The Gallery at 109 E. 6th St.  I.U. also has it's art museum and School of Fine Arts 
Gallery as well as the Lilly Library. Original art can also be found lining the walls 
of such establishments as Michael's Uptown Cafe, The Laughing Planet, The Runciple Spoon, 
and many other places.</h3>
