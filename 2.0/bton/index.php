<h3>

So since I've been in <a href="http://www.bloomington.in.gov/">
Bloomington</a> for the past 19 or so years, I guess I must like it here.  
Coming down fresh out of high school from Indianapolis, Bloomington seemed 
like paradise.  Home to Indiana University, Bloomington offers the cultural 
enrichment and nightlife of a University combined with the closeness of a 
small town.  I've gone without a car for years on end without feeling 
any great inconvenience.  Most residents say what they like about 
Bloomington is the fact that there is enough going on here to have a reasonable nightlife, but still small enough that you know people almost every time you walk 
downtown.  Another attraction is the proximity of 
<a href="http://dcwi.com/~eric/goot/yellowsf.htm">Yellowwood</a> National 
Forest, only about 20 minutes from downtown.

