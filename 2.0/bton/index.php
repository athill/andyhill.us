<a name="Food and Fun" id="Food and Fun"><h2>Food and Fun</h2></a>

If you ask me, the best place to go to eat is the 
<a href="http://www.michaelsuptowncafe.com/">Uptown Cafe</a>, but I would also recommend Lennie's, the 
Laughing Planet, The Trojan Horse, Brave New Deli, Bear's Place, and 
investigating the wide array of ethnic restaurants in the Kirkwood area.  
For coffee, I usually go to Soma and sometimes the Runciple Spoon.  To see 
music. 
I usually go to 2nd Story (um, not anymore, they're closed)  
or <a href="http://www.festered.net/">Uncle Festers</a>  unless there's 
a good band at the <a href=" http://www.thebluebird.ws">Bluebird</a>. 
Other places for music include <a href="http://www.bearsplacebar.com/">
Bear's Place</a> (which 
features Jazz Fables, a weekly Jazz show put on every Thursday), 
<a href="http://www.bloomington.in.us/~rhinos/">Rhino's</a>
all-ages, <a href="http://arthospital.net/home.php">The Art Hospital</a>,  
<a href="http://landlockedmusic.com/">Landlocked Music</a>, 
The John Waldron 
<a href="http://www.artlives.org/">Arts</a> Center, and The 
<a href="http://www.buskirkchumley.org/">Buskirk-Chumley</a>(BusChum) 
Theatre.  
The Waldron and the Buskirk 
are also good places to see plays, which you can also find at the 
<a href="http://www.newplays.org/">Bloomington Playwrites' Project</a>, 
on campus at <a href="http://www.indiana.edu/~thtr/productions/">I.U.</a>, 
and in <a href="http://www.mcct.org/">3rd Street Park</a> 
in the summer.  <a href="http://www.bearsplacebar.com/">Bear's Place</a>, 
together with I.U. and 
<a href="http://www.thecinemat.com/">The Cinemat (also a great movie 
rental store)</a>, also hosts the <a href="http://www.theryder.com/">Ryder</a> Film Series, which specializes in the coolest 
second-run/foreign/classic films out there. If you're looking to curl up
with a good book, 
<a href="http://www.bloomingpedia.org/wiki/Howard's_Bookstore">
Howard's Bookstore</a>, on the South side of the square, has a 
good selection of books, as well as a cat or two wandering around; The Book 
Corner, at the corner of Kirkwood and Walnut, has some fine books; and 
<a href="http://www.boxcarbooks.org">Boxcar Books</a> has something for the 
more politically minded. Alternative videos and DVDs can be found at Plan 9, 
on 6th between Walnut and Washington, and a new video store, 
<a href="http://www.thecinemat.com">Cinemat</a> offers &quot;Fine Video &amp; DVD 
Rentals&quot; as well as movie screenings.  

