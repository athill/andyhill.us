<a name="I.U." id="I.U."><h2>Indiana University</h2></a>

I don't want to discount the cultural offerings of 
<a href="http://www.indiana.edu">I.U.</a>, though I think 
supporting local stuff to be important.  The 
<a href="http://www.music.indiana.edu/flash/virtualtour/noflash/mac/">
Musical Arts Center(MAC)</a> is the 
only place I know of to see a symphony or an opera in town.  They also bring 
Broadway plays and world-famous entertainers to the 
<a href="http://www.iuauditorium.com/">I.U. Auditorium</a>	.  
Smaller up-and-coming bands are often booked to play in the 
<a href="http://www.imu.indiana.edu/">I.U. Memorial 
Union</a>.  Art from throughout the world and history can be found in the 
<a href="http://www.indiana.edu/~iuam/">I.U. 
Art Museum</a> and student work and travelling shows can be found in 
the 
<a href="http://www.indiana.edu/~sofa/2005/">School of Fine Arts(SOFA) 
Gallery</a>.  Student recitals can be heard on 
Sundays at 
<a href="http://www.music.indiana.edu/flash/virtualtour/noflash/simon/auer.shtml">
Auer Hall</a>.  

