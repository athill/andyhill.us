<a name="radio" id="radio"><h2>Radio</h2></a>

One of the greatest things about Bloomington is that we have a 
community supported, volunteer-powered radion station,  
<a href="http://wfhb.org/">WFHB</a>, at 91.3 
(<a href="http://129.79.21.137:9198/wfhb_live_hi.m3u">stream</a>) and 
98.1 FM. They play a lot of cool stuff that you don't hear on commercial 
radio.  Broadcasting from 
I.U., we have <a href="http://www.indiana.edu/~wfiu/">WFIU</a> 
at 103.7 FM, offering NPR content and 
<a href="http://www.wiux.org/new/">WIUX</a> at 100.3 FM 
(<a href="http://hannibal.ucs.indiana.edu:8080/wiuxhigh.m3u">stream</a>), 
offering Pure Student Radio.  
If you're into commercial, you need help. Just kidding. We have  
<a href="http://www.wbwb.com">B97</a>(WBWB) as I guess WTTS Moved to Indy. 

